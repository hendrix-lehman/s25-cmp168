class Person {

  private int numOffspring;

  public Person() {
    numOffspring = 0;
  }

  public Person(int numOffspring) {
    this.numOffspring = numOffspring;
  }

  public int getNumOffspring() {
    return numOffspring;
  }

  public void setNumOffspring(int numOffspring) {
    this.numOffspring = numOffspring;
  }
}
